package com.github.dantezitello.weatherapp.controller;

import com.github.dantezitello.weatherapp.controller.charting.ChartResponse;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.context.request.WebRequest;

@ControllerAdvice
public class GlobalControllerExceptionHandler {
    private static final Logger logger = LoggerFactory.getLogger(GlobalControllerExceptionHandler.class);


    @ExceptionHandler
    public ResponseEntity<ChartResponse> resourceNotFoundException(Exception ex, WebRequest request) {
        logger.error("Encountered error", ex);


        return ResponseEntity.internalServerError().body( ChartResponse.createErrorResponse(ex) );
    }

}
